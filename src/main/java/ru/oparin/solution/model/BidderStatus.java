package ru.oparin.solution.model;

/**
 * Статус автоматики биддера (расписание), не путать со статусом РК в WB.
 */
public enum BidderStatus {
    /** Расписание выключено вручную. */
    OFF,
    /** Нет права на «Управление РК» (нет подписки и не клиент агентства). */
    NO_ACCESS,
    /** Расписание включено, но слотов на календаре нет. */
    NO_SLOTS,
    /** Вне слота или запуск WB ещё в очереди. */
    WAITING,
    /** В слоте, исчерпан лимит бюджета слота. */
    SLOT_LIMIT,
    /** В слоте, РК активна в WB. */
    RUNNING
}
